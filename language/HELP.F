De plus amples explications peuvent etre demandees en tapant HELP et 
le nom de la commande en question, donc par exemple: HELP CHECK.
L'execution d'une commande peut a tout moment etre avortee par l'envoi
d'une <ENTER> ou <RETURN> sans lettre. La fin d'execution peut eventuelle-
ment etre temperee par la memorisation intermediaire dans les noeuds NET/ROM.


