Le process d'une demande peut etre interrompu toujours par <ENTER> ou <CR>.
L'annulation peut etre prolongee par un effet de buffer entre les digis.

On peut entrer plusieures commandes separees par point-virgule.
Elles seront traitees dans l'ordre.

